public class Increment {
    public static void main(String[] args) {
        int i=0;
        System.out.println(i++); //0
        i++;
        System.out.println(i++);  //2
        System.out.println(i);    //3
        System.out.println(i++);  //3
        
        for(int k=0; k<5; k++) {
            System.out.println("for loop  "+k);}

        int j=10;
        while(j>0) {
            System.out.println("while loop  "+j);
            j--;
        
        }
    }
}

            
                


